const a = 'JavaScript'.slice(0,4); 
console.log(a) // 결과 Java

const b = 'JavaScript'.slice(0); // 두번째 인수 생략하면 문자열의 마지막까지 추출
console.log(b) // 결과 JavaScript

const c = '나의 노트북'.slice(3, -1); // 3은 '노' -1 한 칸 더 가면 '트' 
console.log(c) // 결과 노트

const d = '나의 노트북'.slice(-6, -4);
console.log(d) // 결과 나의

