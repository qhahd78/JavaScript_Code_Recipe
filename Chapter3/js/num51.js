// 배열 요소 삭제 

const array1 = ['사과', '귤', '바나나'];
const shiftedValue = array1.shift(); //첫 위치 요소 삭제 
console.log(shiftedValue); // 삭제된 요소 출ㄹ력 
console.log(array1); // 삭제 후 배열 출력 

const array2 = ['사과', '귤', '바나나'];
const poppedValue = array2.pop(); // 마지막 위치 요소 삭제 
console.log(poppedValue); // 삭제된 요소 출력 
console.log(array2); // 삭제 후 배열 출력 